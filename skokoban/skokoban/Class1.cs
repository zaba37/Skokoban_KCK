        public static void printFrame()
        {
            Console.ForegroundColor = ConsoleColor.Yellow;
            Console.CursorTop = 0;

            for (int i = 0; i < 159; i++)
            {
                Console.Write("▓");
            }

            Console.Write("\n");

            for (int i = 0; i < 47; i++)
            {
                Console.WriteLine("▓                                                                                                                                                             ▓");
            }

            for (int i = 0; i < 159; i++)
            {
                Console.Write("▓");
            }
        }

